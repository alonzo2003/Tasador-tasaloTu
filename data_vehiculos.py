# FUNCIÓN PARA CARGAR EN DB
# ══════════════════════════════════════════════

def cargar_vehiculos_ampliados(session):
    """
    Carga toda la data ampliada de vehículos en la DB.
    Seguro de llamar múltiples veces — no duplica datos.
    """
    from base_datos import Marca, Modelo, Vehiculo
    from datetime import datetime

    total_marcas   = 0
    total_modelos  = 0
    total_vehiculos = 0

    for data_marca in VEHICULOS_RD:
        # Buscar o crear marca
        marca = session.query(Marca).filter(
            Marca.nombre == data_marca["nombre"]
        ).first()

        if not marca:
            marca = Marca(
                nombre   = data_marca["nombre"],
                pais     = data_marca["pais"],
                segmento = data_marca["segmento"]
            )
            session.add(marca)
            session.flush()
            total_marcas += 1

        for data_modelo in data_marca["modelos"]:
            # Buscar o crear modelo
            modelo = session.query(Modelo).filter(
                Modelo.marca_id == marca.id,
                Modelo.nombre   == data_modelo["nombre"]
            ).first()

            if not modelo:
                modelo = Modelo(
                    marca_id = marca.id,
                    nombre   = data_modelo["nombre"],
                    tipo     = data_modelo["tipo"],
                    traccion = data_modelo["traccion"]
                )
                session.add(modelo)
                session.flush()
                total_modelos += 1

            for anio, precio in data_modelo["años"].items():
                # Buscar o crear vehículo por año
                vehiculo = session.query(Vehiculo).filter(
                    Vehiculo.modelo_id == modelo.id,
                    Vehiculo.anio      == anio
                ).first()

                if not vehiculo:
                    vehiculo = Vehiculo(
                        modelo_id       = modelo.id,
                        anio            = anio,
                        precio_base_usd = float(precio),
                        fuente          = "data_rd_v2",
                        fecha_precio    = datetime.utcnow()
                    )
                    session.add(vehiculo)
                    total_vehiculos += 1
                else:
                    # Actualizar precio si ya existe
                    vehiculo.precio_base_usd = float(precio)
                    vehiculo.fuente          = "data_rd_v2"

    session.commit()
    print(f"✅ Data ampliada cargada:")
    print(f"   {total_marcas} marcas nuevas")
    print(f"   {total_modelos} modelos nuevos")
    print(f"   {total_vehiculos} registros de precio nuevos")
    return total_marcas, total_modelos, total_vehiculos


